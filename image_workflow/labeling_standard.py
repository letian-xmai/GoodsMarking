from __future__ import annotations

from pathlib import Path


PRODUCT_LABELING_STANDARD = """# 商品图片标注标准

## 正样本标准
- 画面只包含一个实际商品实例，商品主体清晰、完整，边界和背景有明确区分。
- 接受实拍手持、立放、横放、顶部、正背面等视角，但必须能代表商品本身。
- 与建模参考图相比，商品形态、包装、瓶身/标签/瓶盖等关键结构不能明显不一致。
## 高风险降权项
- 小于 10KB、边缘裁切、疑似白底、局部视角不是绝对负样本；若人工标注仍认为主体完整清晰，应进入低分或人工复核，而不是直接删除。
- 商品边界弱、轻微模糊、连续帧相似时优先降权；只有明显无法判断商品、严重重复或严重不完整时才排除。
## 负样本排除标准
- 排除纯白底建模图、商品极小、严重模糊、严重裁切或主体明显不完整图片。
- 排除同画面两个商品、第二瓶、第二瓶盖、重复商品局部、连续帧高度重复图片。
- 排除商品与背景边界不清、局部特写无法判断完整商品、与参考商品差异过大的图片。
## 训练和生产约束
- 模型只学习通用质量标准，不学习 outward_code、URL 模式或某个具体商品外观。
- outward_code 只用于分组切分和评估，不能作为模型输入特征。
- 最终筛选不足 40 张时标记 shortfall，不用明显重复、模糊或不完整图片硬凑数量。
- 模型输出分为自动选中、自动排除、人工复核三类；人工复核灰区不计入全自动一致率。
"""


def write_product_labeling_standard(path: str | Path) -> None:
    Path(path).write_text(PRODUCT_LABELING_STANDARD, encoding="utf-8")
